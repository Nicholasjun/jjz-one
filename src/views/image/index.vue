<template>
  <div class="container">
    <el-card>
      <div slot="header">
        <my-bread>素材管理</my-bread>
      </div>
      <div class="btn-box">
        <!-- <el-radio-group v-model="reqParams.collect" size="small"> -->
        <el-radio-group v-model="reqParams.collect" @change="changeCollect" size="small">
          <el-radio-button :label="false">全部</el-radio-button>
          <el-radio-button :label="true">收藏</el-radio-button>
        </el-radio-group>
        <el-button @click="openDialog" type="success" size="small" style="float:right">添加素材</el-button>
      </div>
      <div class="img-list">
        <div class="img-item" v-for="item in images" :key="item.id">
          <img :src="item.url" alt />
          <div class="footer" v-show="!reqParams.collect">
            <span
              @click="toggleCollect(item)"
              class="el-icon-star-off"
              :class="{selected:item.is_collected}"
            ></span>
            <span @click="deleteImage(item.id)" class="el-icon-delete"></span>
          </div>
        </div>
      </div>
      <!-- total为总条数 -->
      <!-- page-size为一页几条数据 -->
      <!-- current-page为当前选中的是那一页 -->
      <!-- @current-change为点击分页重新去后台取数据的方法 -->
      <el-pagination
        v-if="total > reqParams.per_page"
        background
        layout="prev, pager, next"
        :total="total"
        :page-size="reqParams.per_pag"
        :current-page="reqParams.page"
        @current-change="changePager"
      ></el-pagination>
    </el-card>
    <el-dialog title="添加素材" :visible.sync="dialogVisible" width="300px">
      <!-- class="avatar-uploader"为自己保存全局样式 -->
      <!-- action请求地址(上传图片的接口地址) 完整路径 -->
      <!-- headers设置请求头,携带token信息 -->
      <!-- :on-success="handleAvatarSuccess" 上传成功的回调函数 -->
      <!-- imageUrl数据 -->
      <!-- 默认为空，看到上传按钮 -->
      <!-- 当上传成功赋值图片地址，看到图片，src指定图片地址，预览效果。 -->
      <!-- name指定提交给后台的文件的字段名称 默认值为file-->
      <el-upload
        class="avatar-uploader"
        action="http://ttapi.research.itcast.cn/mp/v1_0/user/images"
        name="image"
        :headers="headers"
        :show-file-list="false"
        :on-success="handleSuccess"
      >
        <img v-if="imageUrl" :src="imageUrl" class="avatar" />
        <i v-else class="el-icon-plus avatar-uploader-icon"></i>
      </el-upload>
      <span slot="footer" class="dialog-footer">
        <el-button @click="dialogVisible = false">取 消</el-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
import store from '@/store'
export default {
  data () {
    // 请求参数
    return {
      reqParams: {
        // 接收是全部还是收藏
        collect: false,
        // 当前显示的那一页
        page: 1,
        // 默认一页几条
        per_page: 10
      },
      // 素材列表
      images: [],
      // 总条数
      total: 0,
      // 控制对话框显示隐藏
      dialogVisible: false,
      // 上传成功后图片的地址
      imageUrl: null,
      headers: {
        Authorization: `Bearer ${store.getUser().token}`
      }
    }
  },
  created () {
    // 获取素材列表
    this.getImages()
  },
  methods: {
    // 删除
    deleteImage (id) {
      // 加个确认框
      this.$confirm('此操作将永久删除该文件, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      })
        .then(async () => {
          await this.$http.delete('user/images/' + id)
          this.$message.success('删除成功')
          this.getImages()
        })
        .catch(() => {
          this.$message.success('取消删除成功')
        })
    },
    // 切换收藏
    async toggleCollect (item) {
      console.log(item.is_collected)

      const {
        // 因为有async,所以可以给data进行解构赋值

        data: { data }
      } = await this.$http.put(`user/images/${item.id}`, {
        // collect的值为false,当前元素有样式的时候取反,就是false
        collect: !item.is_collected
      })
      // 提交成功后是true是成功,false就是取消
      this.$message.success(data.collect ? '添加收藏成功' : '取消收藏成功')
      // 更改当前图片的状态(当前样式是false)
      item.is_collected = data.collect
    },
    // 上传成功
    handleSuccess (res) {
      // res为后端给的响应主体
      // 获取上传成功的图片地址
      // 1.预览
      this.imageUrl = res.data.url
      // 2.提示
      this.$message.success('上传成功')

      window.setTimeout(() => {
        // 3.    2s秒后  关闭对话框
        this.dialogVisible = false
        // 4.    2s秒后  更新列表
        this.getImages()
        // 5.回到第一页
        this.reqParams.page = 1
      }, 2000)
    },
    // 打开对话框
    openDialog () {
      // 打开对话框之前,先清空预览图的地址
      this.imageUrl = null
      this.dialogVisible = true
    },
    changeCollect () {
      // 每次发生改变的时候页数总是第一页
      this.reqParams.page = 1
      // 重新接收数据,接收是收藏了的数据
      this.getImages()
    },
    changePager (newPage) {
      this.reqParams.page = newPage
      this.getImages()
    },
    async getImages () {
      const {
        data: { data }
      } = await this.$http.get('user/images', { params: this.reqParams })
      // 赋值给图片列表
      this.images = data.results
      this.total = data.total_count
    }
  }
}
</script>

